public class Basic_concept {
    int length, breadth, height; // Class variables (attributes)



    public static void main(String[] args) {
        Basic_concept b = new Basic_concept(); // Object created
        b.length = 10;  // Values must be set manually
        b.breadth = 5;
        b.height = 8;

        System.out.println("Length: " + b.length);
        System.out.println("Breadth: " + b.breadth);
        System.out.println("Height: " + b.height);
    }
}

    

